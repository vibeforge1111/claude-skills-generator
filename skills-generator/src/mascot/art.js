/**
 * Generate mascot art with custom name
 */
export function generateMascotArt(template, customName) {
  const mascot = CHILD_MASCOT_TEMPLATES[template] || CHILD_MASCOT_TEMPLATES.generic;
  const art = {};

  for (const [state, ascii] of Object.entries(mascot.art)) {
    art[state] = ascii + `\n   ${customName || mascot.name}`;
  }

  return art;
}

/**
 * Get HOOT-9000 art for a specific state
 */
export function getHootArt(state = 'normal') {
  return HOOT_STATES[state] || HOOT_STATES.normal;
}

